    private void botonConsutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsutarActionPerformed
        try {
            Boolean b = (campoFechaDesde.getDate() != null) && (campoFechaHasta.getDate() != null) && (!campoProducto.getText().isEmpty());
            if(b) {
                Date desde = campoFechaDesde.getDate();
                Date hasta = campoFechaHasta.getDate();
                Producto producto = productoJpaController.buscarProductoPorCodigo(campoProducto.getText());
                Double varianza = productoJpaController.varianzaDelPrecioDeUnProducto(desde, hasta, producto);
                areaTexto.setText("Desde el " + desde.toLocaleString() + " hasta el " + hasta.toLocaleString() + " el precio del producto " + producto.getCodigo() + " varió un " + varianza);
            } else {
                throw new Exception("Campos incompletos.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonConsutarActionPerformed

