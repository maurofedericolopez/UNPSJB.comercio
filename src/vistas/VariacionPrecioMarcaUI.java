    private void botonConsutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsutarActionPerformed
        try {
            Boolean b = (campoFechaDesde.getDate() != null) && (campoFechaHasta.getDate() != null) && (campoMarca.getSelectedItem() != null);
            if(b) {
                Date desde = campoFechaDesde.getDate();
                Date hasta = campoFechaHasta.getDate();
                Marca marca = (Marca) campoMarca.getSelectedItem();
                ArrayList<Producto> productos = productoJpaController.obtenerProductosPorMarca(marca);
                modeloTabla.actualizarTabla(desde, hasta, productos);
            } else {
                throw new Exception("Campos incompletos.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonConsutarActionPerformed

