    private void campoAlmacenItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_campoAlmacenItemStateChanged
        if(evt.getStateChange() == ItemEvent.SELECTED) {
            Almacen almacen = (Almacen) campoAlmacen.getSelectedItem();
            ArrayList<Transferencia> transferencias = transferenciaJpaController.obtenerTransferenciasProximasAVencerDeAlmacen(almacen);
            lotesProximosAVencerTableModel.setTransferencias(transferencias);
        }
    }//GEN-LAST:event_campoAlmacenItemStateChanged

    private void campoPuntoDeVentaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_campoPuntoDeVentaItemStateChanged
        if(evt.getStateChange() == ItemEvent.SELECTED) {
            PuntoVenta puntoDeVenta = (PuntoVenta) campoPuntoDeVenta.getSelectedItem();
            ArrayList<Transferencia> transferencias = transferenciaJpaController.obtenerTransferenciasProximasAVencerDePuntoDeVenta(puntoDeVenta);
            lotesProximosAVencerTableModel.setTransferencias(transferencias);
        }
    }//GEN-LAST:event_campoPuntoDeVentaItemStateChanged

